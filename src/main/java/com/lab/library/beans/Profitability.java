package com.lab.library.beans;

public class Profitability {
    private double revenue;
    private int booksAmount;
    private int readersAmount;

    public double getRevenue() {
        return revenue;
    }

    public void setRevenue(double revenue) {
        this.revenue = revenue;
    }

    public int getBooksAmount() {
        return booksAmount;
    }

    public void setBooksAmount(int booksAmount) {
        this.booksAmount = booksAmount;
    }

    public int getReadersAmount() {
        return readersAmount;
    }

    public void setReadersAmount(int readersAmount) {
        this.readersAmount = readersAmount;
    }
}
